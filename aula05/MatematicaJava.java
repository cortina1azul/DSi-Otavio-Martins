public class MatematicaJava {
    public static void main(String[] args) {

      System.out.println("\\\\ Operações matemáticas \\\\");

      int x = 670;
      int y = 67;
  
      System.out.println(x + y); // 737
      System.out.println(x - y); // 603
      System.out.println(x * y); // 44890
      System.out.println(x / y); // 10
      System.out.println(x % y); // 0
  
      int z = 68;
      ++z;
      System.out.println(z); // 69
      --z;
      System.out.println(z); // 67

      System.out.println("\\\\ Diferenças de inteiros e decimais na divisão Java \\\\");

      int a = 34;
      int b = 11;
      System.out.println(a / b);   // Integer division, result is 3
      
      

      double c = 11.0d;
      double d = 34.0d;
      System.out.println(c / d);   // Decimal division, result is 3.333...


    }
  }
  