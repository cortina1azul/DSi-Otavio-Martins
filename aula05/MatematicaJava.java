public class MatematicaJava {
    public static void main(String[] args) {
      int x = 670;
      int y = 67;
  
      System.out.println(x + y); // 737
      System.out.println(x - y); // 603
      System.out.println(x * y); // 44890
      System.out.println(x / y); // 10
      System.out.println(x % y); // 0
  
      int z = 68;
      ++z;
      System.out.println(z); // 69
      --z;
      System.out.println(z); // 67
    }
  }
  